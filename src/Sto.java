public class Sto {
    private int broj;
    private int stolice;
    private int x;
    private int y;

    public Sto(int broj, int stolice, int x, int y) {
        this.broj = broj;
        this.stolice = stolice;
        this.x = x;
        this.y = y;
    }

    public int getBroj() {
        return broj;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
